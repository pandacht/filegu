# Default language
_current = "en"


def set_lang(code: str):
    global _current
    if code in STRINGS:
        _current = code


def t(key: str, **kwargs) -> str:
    """
    Get a translated string by key.
    Supports keyword formatting: t("search.status_results", n=42) → "42 result(s) found"
    Falls back to English if key missing in current language.
    Falls back to the key itself if not found anywhere.
    """
    lang_dict = STRINGS.get(_current, STRINGS["en"])
    text = lang_dict.get(key) or STRINGS["en"].get(key) or key
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text


def available_languages() -> list[tuple[str, str]]:
    """Return list of (code, display_name) tuples."""
    names = {
        "en": "English",
        "de": "Deutsch",
        "ru": "Русский",
    }
    return [(code, names.get(code, code)) for code in STRINGS]